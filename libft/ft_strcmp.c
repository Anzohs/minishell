#include "libft.h"

int	ft_strcmp(char *c, char *d)
{
	int	i;

	i = 0;
	if (!c && !d)
		return (0);
	if (!c || !d)
		return (1);
	while (c[i] == d[i] && d[i] && c[i])
		i++;
	if (d[i] == c[i])
		return (0);
	return (1);
}
